const POINT_COUNT = 96;
const SEGMENT_MS = 1800;
const HOLD_FRAC = 0.18;
/** Skip static hold on start so short frame loaders still show morph motion. */
const HOLD_SKIP_MS = SEGMENT_MS * (HOLD_FRAC + 0.02);

let sampleRoot = null;
let samplePathEl = null;
let preparedShapes = null;
let preparePromise = null;

function easeInOutSine(t) {
  return -(Math.cos(Math.PI * t) - 1) / 2;
}

function prefersReducedMotion() {
  return window.matchMedia('(prefers-reduced-motion: reduce)').matches;
}

function ensureSampleRoot() {
  if (sampleRoot) return;
  sampleRoot = document.createElementNS(SVG_NS, 'svg');
  sampleRoot.setAttribute('width', '0');
  sampleRoot.setAttribute('height', '0');
  sampleRoot.style.cssText = 'position:absolute;overflow:hidden;visibility:hidden;pointer-events:none';
  samplePathEl = document.createElementNS(SVG_NS, 'path');
  sampleRoot.appendChild(samplePathEl);
  document.body.appendChild(sampleRoot);
}

function normalizePoints(points) {
  let minX = Infinity;
  let minY = Infinity;
  let maxX = -Infinity;
  let maxY = -Infinity;

  points.forEach(function (p) {
    minX = Math.min(minX, p.x);
    minY = Math.min(minY, p.y);
    maxX = Math.max(maxX, p.x);
    maxY = Math.max(maxY, p.y);
  });

  const bw = Math.max(maxX - minX, 1e-6);
  const bh = Math.max(maxY - minY, 1e-6);
  const pad = 0.06;
  const inner = 1 - pad * 2;
  const scale = inner / Math.max(bw, bh);
  const dw = bw * scale;
  const dh = bh * scale;
  const ox = pad + (inner - dw) * 0.5;
  const oy = pad + (inner - dh) * 0.5;

  return points.map(function (p) {
    return {
      x: ox + (p.x - minX) * scale,
      y: oy + (p.y - minY) * scale,
    };
  });
}

function sampleShape(shape) {
  ensureSampleRoot();
  const vb = shape.viewBox;
  sampleRoot.setAttribute('viewBox', vb.join(' '));
  samplePathEl.setAttribute('d', shape.d);
  const total = samplePathEl.getTotalLength();
  const points = [];
  for (let i = 0; i < POINT_COUNT; i += 1) {
    const pt = samplePathEl.getPointAtLength((i / POINT_COUNT) * total);
    points.push({ x: pt.x, y: pt.y });
  }
  return normalizePoints(points);
}

function alignPoints(reference, candidate) {
  let bestOffset = 0;
  let bestScore = Infinity;
  for (let offset = 0; offset < POINT_COUNT; offset += 1) {
    let score = 0;
    for (let i = 0; i < POINT_COUNT; i += 1) {
      const other = candidate[(i + offset) % POINT_COUNT];
      const dx = reference[i].x - other.x;
      const dy = reference[i].y - other.y;
      score += dx * dx + dy * dy;
    }
    if (score < bestScore) {
      bestScore = score;
      bestOffset = offset;
    }
  }
  const aligned = new Array(POINT_COUNT);
  for (let i = 0; i < POINT_COUNT; i += 1) {
    aligned[i] = candidate[(i + bestOffset) % POINT_COUNT];
  }
  return aligned;
}

function lerpPoints(from, to, t) {
  const out = new Array(POINT_COUNT);
  for (let i = 0; i < POINT_COUNT; i += 1) {
    out[i] = {
      x: from[i].x + (to[i].x - from[i].x) * t,
      y: from[i].y + (to[i].y - from[i].y) * t,
    };
  }
  return out;
}

function pointsToPath(points) {
  if (!points.length) return '';
  let d = 'M ' + points[0].x + ' ' + points[0].y;
  for (let i = 1; i < points.length; i += 1) {
    d += ' L ' + points[i].x + ' ' + points[i].y;
  }
  return d + ' Z';
}

function prepareShapes() {
  if (preparedShapes) return Promise.resolve(preparedShapes);
  if (preparePromise) return preparePromise;

  preparePromise = Promise.resolve().then(function () {
    const raw = VESSEL_SHAPES.map(sampleShape);
    const aligned = [raw[0]];
    for (let i = 1; i < raw.length; i += 1) {
      aligned.push(alignPoints(raw[0], raw[i]));
    }
    preparedShapes = aligned;
    return aligned;
  });

  return preparePromise;
}

function createMorphSvg() {
  const svg = document.createElementNS(SVG_NS, 'svg');
  svg.setAttribute('viewBox', '0 0 1 1');
  svg.setAttribute('preserveAspectRatio', 'xMidYMid meet');
  svg.classList.add('pagmar-vessel-morph__svg');
  svg.setAttribute('aria-hidden', 'true');

  const path = document.createElementNS(SVG_NS, 'path');
  path.setAttribute('fill', 'none');
  path.setAttribute('stroke', 'currentColor');
  path.setAttribute('stroke-width', '0.022');
  path.setAttribute('stroke-linejoin', 'round');
  path.setAttribute('stroke-linecap', 'round');
  svg.appendChild(path);

  return { svg: svg, path: path };
}

/**
 * @param {HTMLElement} container
 * @returns {{ start: () => void, stop: () => void, destroy: () => void }}
 */
export function mountVesselMorph(container) {
  const wrap = document.createElement('div');
  wrap.className = 'pagmar-vessel-morph';
  const graphic = createMorphSvg();
  wrap.appendChild(graphic.svg);
  container.appendChild(wrap);

  let rafId = 0;
  let running = false;
  let shapes = preparedShapes;

  function renderAt(elapsed) {
    if (!shapes) return;
    const seg = SEGMENT_MS;
    const total = seg * shapes.length;
    const loopT = elapsed % total;
    const index = Math.floor(loopT / seg);
    const local = (loopT % seg) / seg;
    const morphStart = HOLD_FRAC;
    const from = shapes[index];
    const to = shapes[(index + 1) % shapes.length];
    let blend = 0;
    if (local > morphStart) {
      blend = easeInOutSine((local - morphStart) / (1 - morphStart));
    }
    graphic.path.setAttribute('d', pointsToPath(lerpPoints(from, to, blend)));
  }

  function tick(startedAt) {
    if (!running) return;
    renderAt(performance.now() - startedAt);
    rafId = requestAnimationFrame(function () {
      tick(startedAt);
    });
  }

  function beginAnimation(ready, startedAt) {
    shapes = ready;
    if (!running) return;
    if (prefersReducedMotion()) {
      graphic.path.setAttribute('d', pointsToPath(ready[0]));
      return;
    }
    renderAt(performance.now() - startedAt);
    rafId = requestAnimationFrame(function () {
      tick(startedAt);
    });
  }

  function start() {
    stop();
    running = true;
    const startedAt = performance.now() - HOLD_SKIP_MS;
    if (preparedShapes) {
      beginAnimation(preparedShapes, startedAt);
      return;
    }
    prepareShapes().then(function (ready) {
      beginAnimation(ready, startedAt);
    });
  }

  function stop() {
    running = false;
    if (rafId) {
      cancelAnimationFrame(rafId);
      rafId = 0;
    }
  }

  function destroy() {
    stop();
    wrap.remove();
  }

  return { start: start, stop: stop, destroy: destroy };
}

export function preloadVesselMorph() {
  return prepareShapes();
}

